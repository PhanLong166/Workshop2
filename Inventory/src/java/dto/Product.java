package dto;

/**
 *
 * @author Admin
 */
public class Product {
    private String ID;
    private String name;
    private String type;
    private int quantity;
    private int price;

    public Product() {
    }

    public Product(String ID, String name, String type, int quantity, int price) {
        this.ID = ID;
        this.name = name;
        this.type = type;
        this.quantity = quantity;
        this.price = price;
    }

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
    
    
}
